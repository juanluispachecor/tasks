package org.up.ahitask.entities;

public class Person {
	protected int id;
	protected int firstName;
	protected int secondName;
	protected int lastName;
	protected int email;
	protected int phone;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getFirstName() {
		return firstName;
	}
	public void setFirstName(int firstName) {
		this.firstName = firstName;
	}
	public int getSecondName() {
		return secondName;
	}
	public void setSecondName(int secondName) {
		this.secondName = secondName;
	}
	public int getLastName() {
		return lastName;
	}
	public void setLastName(int lastName) {
		this.lastName = lastName;
	}
	public int getEmail() {
		return email;
	}
	public void setEmail(int email) {
		this.email = email;
	}
	public int getPhone() {
		return phone;
	}
	public void setPhone(int phone) {
		this.phone = phone;
	}

           

}
